'use client';

import { useEffect, useState } from 'react';

interface ToastProps {
  message: string;
  type: 'success' | 'error' | 'info';
  isVisible: boolean;
  onClose: () => void;
}

export function Toast({ message, type, isVisible, onClose }: ToastProps) {
  useEffect(() => {
    if (isVisible) {
      const timer = setTimeout(() => {
        onClose();
      }, 4000);
      return () => clearTimeout(timer);
    }
  }, [isVisible, onClose]);

  if (!isVisible) return null;

  const getToastStyles = () => {
    switch (type) {
      case 'success':
        return 'bg-green-500/20 border-green-400 text-green-100';
      case 'error':
        return 'bg-red-500/20 border-red-400 text-red-100';
      case 'info':
        return 'bg-blue-500/20 border-blue-400 text-blue-100';
      default:
        return 'bg-gray-500/20 border-gray-400 text-gray-100';
    }
  };

  const getIcon = () => {
    switch (type) {
      case 'success':
        return '‚úÖ';
      case 'error':
        return '‚ùå';
      case 'info':
        return '‚ÑπÔ∏è';
      default:
        return 'üì¢';
    }
  };

  return (
    <div className="fixed top-4 right-4 z-50 animate-in slide-in-from-right duration-300">
      <div className={`glass border-2 rounded-xl p-4 max-w-sm ${getToastStyles()}`}>
        <div className="flex items-center gap-3">
          <span className="text-xl">{getIcon()}</span>
          <p className="flex-1 text-sm font-medium">{message}</p>
          <button
            onClick={onClose}
            className="text-white/60 hover:text-white transition-colors"
          >
            √ó
          </button>
        </div>
      </div>
    </div>
  );
}

// Toast context and hook for easy usage
import { createContext, useContext } from 'react';

interface ToastContextType {
  showToast: (message: string, type: 'success' | 'error' | 'info') => void;
}

const ToastContext = createContext<ToastContextType | null>(null);

export function ToastProvider({ children }: { children: React.ReactNode }) {
  const [toast, setToast] = useState<{
    message: string;
    type: 'success' | 'error' | 'info';
    isVisible: boolean;
  }>({
    message: '',
    type: 'info',
    isVisible: false
  });

  const showToast = (message: string, type: 'success' | 'error' | 'info') => {
    setToast({ message, type, isVisible: true });
  };

  const hideToast = () => {
    setToast(prev => ({ ...prev, isVisible: false }));
  };

  return (
    <ToastContext.Provider value={{ showToast }}>
      {children}
      <Toast
        message={toast.message}
        type={toast.type}
        isVisible={toast.isVisible}
        onClose={hideToast}
      />
    </ToastContext.Provider>
  );
}

export function useToast() {
  const context = useContext(ToastContext);
  if (!context) {
    throw new Error('useToast must be used within a ToastProvider');
  }
  return context;
} 